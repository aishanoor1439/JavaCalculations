package com.mycompany.mavenproject22;

public class Calculator {

//Methods
    public int sum(int a, int b) {
        return a + b;
    }

    public int sum(int a, int b, int c) {
        return a + b + c;
    }

    public double sum(double a, double b) {
        return a + b;
    }

    public double sum(int a, double b) {
        return a + b;
    }
}
